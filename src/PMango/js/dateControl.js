function validateDate(fld) {
    var RegExPattern = /^((((0?[1-9]|[12]\d|3[01])[\.\-\/](0?[13578]|1[02])[\.\-\/]((1[6-9]|[2-9]\d)?\d{2}))|((0?[1-9]|[12]\d|30)[\.\-\/](0?[13456789]|1[012])[\.\-\/]((1[6-9]|[2-9]\d)?\d{2}))|((0?[1-9]|1\d|2[0-8])[\.\-\/]0?2[\.\-\/]((1[6-9]|[2-9]\d)?\d{2}))|(29[\.\-\/]0?2[\.\-\/]((1[6-9]|[2-9]\d)?(0[48]|[2468][048]|[13579][26])|((16|[2468][048]|[3579][26])00)|00)))|(((0[1-9]|[12]\d|3[01])(0[13578]|1[02])((1[6-9]|[2-9]\d)?\d{2}))|((0[1-9]|[12]\d|30)(0[13456789]|1[012])((1[6-9]|[2-9]\d)?\d{2}))|((0[1-9]|1\d|2[0-8])02((1[6-9]|[2-9]\d)?\d{2}))|(2902((1[6-9]|[2-9]\d)?(0[48]|[2468][048]|[13579][26])|((16|[2468][048]|[3579][26])00)|00))))$/;
    var errorMessage = 'Please enter a valid date. Format is dd/mm/yyyy.';
    if ((fld.value.match(RegExPattern)) && (fld.value!='')) {
        return true;
    } else {
        alert(errorMessage);
        fld.focus();
        return false;
    } 
}

function compareDate(fld1,fld2)
{
	if(validateDate(fld1)&&validateDate(fld2)){
		var date1=fld1.value.split("/");
		var date2=fld2.value.split("/");
		var concat2=date2[2]+date2[1]+date2[0];
		var concat1=date1[2]+date1[1]+date1[0];
		if(concat2>concat1) return true;
		else{ alert("Start date must before end date.");
		return false;}
	}
	else {
	 //alert("Please enter a valid date. Format is dd/mm/yyyy???.");
	 return false;
	}
}